#include <MentorBitPir.h>


MentorBitPir::MentorBitPir(uint8_t pin_pir){
    _pin_pir = pin_pir;
    pinMode(_pin_pir,INPUT);
}

bool MentorBitPir::lecturaDigital(){
    bool value;
    value = digitalRead(_pin_pir);
    return value;
}